            graduate advisor.
        </p>

        <h3>II. BA/MA Program in Preparation For Adolescent Education (Grades 7-12)</h3>
        <p>
            Students interested in secondary school teaching may pursue a combined BA/MA program in teaching. This
            program requires
            a minimum of 141 credits.
        </p>
        <h4>Admission Requirements for the BA/MA in Teaching:</h4>
        <ol>
            <li>Completion of at least 45 credits with a GPA of 3.0</li>
            <li>Completion of at least 10 credits in mathematics, including a year of calculus (MATH 150 and 155,
                or the
                equivalent), with an average of 2.7 in these major courses. </li>
            <li>Admission to the School of Education.</li>
        </ol>
        <h4>Degree Requirements for the BA/MA in Teaching</h4>
        <p>
            The BA/MA program in the teaching of mathematics includes 48 credits in mathematics (in addition to the
            year of
            calculus described above) and 27 credits in teacher education courses, some of them taken at the
            undergraduate level
            and some at the graduate level. The required math/stat courses are:
            <ol>
                <li>The 33 credits required for the BA in Mathematics or in Statistics, as described above in the
                    sections pertaining
                    to the majors.</li>
                <li>The following 15 graduate credits: MATH 620, 630, 633, 635, and STAT 612 or* 614.</li>
            </ol>
        </p>
        <div class="note">
            <p>
                *Although students are required to take either 612 or STAT 614, students are encouraged to take both
                courses.
            </p>
        </div>
        <p>
            Also required is passing a comprehensive examination in mathematics consisting of three parts covering the
            material in
            MATH 620, 630, and 633. Students may sit for the comprehensive examination only after having taken all
            three courses.
            Students who fail any part of the examination need only retake that part of the examination. A student can
            take any
            part of the comprehensive exam in mathematics twice; after that, a committee formed by the
            Mathematics/Statistics
            Chairperson and consisting of members from the Department of Mathematics and Statistics and the Department
            of
            Curriculum and Teaching will decide if the student can take the exam a third and final time.
        </p>
        <p>
            The student should consult the School of Education section of the graduate college catalog for the sequence
            of teacher
            education courses.
        </p>

