<ol>
    <li>One semester of introductory biology (BIOL 100 or equivalent)</li>
    <li>One year of general chemistry (CHEM 102-105 or equivalent)</li>
    <li>One semester of organic chemistry (CHEM 222 or equivalent)</li>
