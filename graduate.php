<h3>Master of Arts in Adolescent Mathematics Education</h3>
<p>This is a 41- to 42-credit MA program designed for individuals who completed an undergraduate 
    major in mathematics but have little or no background in teacher education. The program is 
    offered in collaboration with the School of Education and leads to New York State initial 
    certification. 
</p>
<h4>Admission Requirements</h4>
<ol>
    <li>A bachelor’s degree from an accredited institution acceptable to Hunter College with an overall cumulative grade point average (GPA) of 3.0.</li>
    <li>24 or more credits in mathematics approved by the mathematics education advisor, including a course in elementary statistics, a year of calculus, and 
        a course in matrix algebra or linear algebra. Applicants may be admitted on the condition that they complete up to 12 credits of additional course work 
        in mathematics. A transcript review will determine which additional credits, if any, are needed.</li>
        <li>A grade point average of at least 2.7 in mathematics courses.</li>
        <li>A general education core in the liberal arts and sciences to include the following in addition to math: 6 credits in English, 6 credits in a language 
            other than English (3 years of high school study in the same foreign language with grades of C or better documented by a high school transcript will 
            meet this requirement), 6 credits in social studies (to include at least one course in U.S. history or U.S. geography), 6 credits in science/technology, 
            3 credits in the arts.</li>
</ol>
<h4>Degree Requirements</h4>
<h5>Education (26-27 credits)</h5>
<p>See the School of Education section of the graduate catalog for specification of courses.</p>

<h5>Mathematics (15 credits)</h5>
<ul>
    <li>MATH 620 Secondary School Mathematics from an Advanced Perspective I</li>
    <li>MATH 630 Secondary School Mathematics from an Advanced Perspective II</li>
    <li>MATH 633* Axiomatic Geometry</li>
    <li>MATH 635* Problem Solving Explorations in Mathematics</li>
    <li>STAT 612*/** Discrete Probability  or  STAT 614*/** Data Analysis Using Statistical Software</li>
</ul>

<h5>Comprehensive Examination</h5>
<p>Also required is passing a comprehensive examination in mathematics consisting of three parts covering the material in MATH 620, 630 and 633. Students may sit 
    for the comprehensive examination only after having taken all three courses. Students who fail any part of the examination need only retake that part of the 
    examination. A student can take any part of the comprehensive exam in mathematics twice; after that, a committee formed by the Mathematics/Statistics 
    Chairperson and consisting of members from the Department of Mathematics and Statistics and the Department of Curriculum and Teaching, will decide if the 
    student can take the exam a third and final time.
</p>

<div class="note">
    <p>*With approval of the graduate mathematics adviser, students who have had substantial coverage of a given area within an undergraduate curriculum may be 
        allowed to replace a course or courses in this category with electives, preferably elective courses at a more advanced level in the same area.
 <br>**Although students are required to take EITHER STAT 612 OR STAT 614, students are encouraged to take both courses.</p>
</div>
