<h3>Track III: Bioinformatics</h3>
<h4>Additional Admission Requirements</h4>
<p>The Bioinformatics track allows students with career plans in the biomedical sciences and without a previous specialization in mathematics 
    to pursue a degree in statistics and applied mathematics. In addition to the above departmental requirements for admission to the MA program 
    in Statistics and Applied Mathematics, it is required that the student have an adequate undergraduate training in biological sciences, 
    chemistry, or related fields, as well as strong computer programming skills. The following track requirements, or their equivalents, are the 
    minimal requirements that the student must meet in order to enter the Bioinformatics track:
