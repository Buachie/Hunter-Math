            <h2>FAQ</h2>
            <br>
            <h3>General Questions</h3>
            <div class="faq-item">
                <p class="question bold">What can I do with math besides teaching and actuarial work? <i class="fas fa-angle-down"></i></p>

                <p class="ans">Mathematics and statistics are used in connection with just about everything – medicine,
                    finance,
                    music, sports,
                    movies, weather, among many other fields.
                    Employers need persons with good analytical skills. Mathematics is well respected as a
                    pre-professional major and
                    as business preparation, especially for students
                    considering the best medical and law schools and business schools.</p>
            </div>
            <div class="faq-item">
                <p class="question bold">What is the accelerated BA/MA program? <i class="fas fa-angle-down"></i></p>
                <p class="ans">The BA/MA program gives a student both a BA and a MA degree with fewer credits than
                    required for
                    obtaining the two
                    degrees separately. There are several
                    BA/MA programs because the BA may be in mathematics or statistics, and the MA may be in pure
                    mathematics, applied
                    mathematics, statistics or mathematics
                    teacher education. Each BA/MA program requires a major, the General Education Requirement, and a
                    master’s sequence.</p>
            </div>
            <div class="faq-item">
                <p class="question bold">Are there assistantships in the graduate programs? <i class="fas fa-angle-down"></i></p>
                <p class="ans">Although there are no assistantships, graduate students have the opportunity of teaching
                    a course in
                    the Department
                    as adjunct lecturers. </p>
            </div>
            <div class="faq-item">
                <p class="question bold">Does the Department have internships? <i class="fas fa-angle-down"></i></p>
                <p class="ans">The Department supports external internships as well as internships at Hunter in various
                    departments
                    such as
                    geography, political science, and psychology. For
                    now, it is expected that students will receive credit for such internships.</p>
            </div>
            <div class="faq-item">
                <p class="question bold">Are there opportunities for undergraduate research? <i class="fas fa-angle-down"></i></p>
                <p class="ans">Currently, the College has the National Science Foundation (NSF) funded Catalyst
                    Scholarship Program
                    in which
                    students receive a stipend and
                    have the opportunity of participating in research projects with full-time faculty mentors. Yearly,
                    talented
                    students are encouraged to apply for a summer Research
                    Experience for Undergraduates (REU) program. The NEWS page of this site mentions some students who
                    have succeeded
                    recently in being admitted to a REU
                    program.</p>
            </div>

            <h3>Registering for Courses</h3>
            <div class="faq-item">
                <p class="question bold">What should I do if I have a question for a departmental advisor but my schedule
                    does not allow
                    me
                    to visit during
                    office hours? <i class="fas fa-angle-down"></i></p>
                <p class="ans">Advising may be done by e-mail. When you e-mail an advisor, be sure to include a topic
                    in the “Subject”
                    field. E-mail without an identifiable source and e-mail
                    without a subject are usually not opened. The e-mail addresses for the departmental advisors are on
                    the CONTACTS
                    page of this site.</p>
            </div>
            <div class="faq-item">
                <p class="question bold">How do I get an evaluation of a course that has been transferred to Hunter? <i class="fas fa-angle-down"></i></p>
                <p class="ans">You must see a departmental advisor. Bring your Hunter transcript, a description of the
                    course
                    transferred and descriptions of its prerequisites. If your schedule
                    does not allow you to see an advisor during office hours, leave the necessary documents in the
                    department office,
                    919East, to be picked up later.</p>
            </div>
            <div class="faq-item">
                <p class="question bold">May I take a math/stat course on permit at another college? <i class="fas fa-angle-down"></i></p>
                <p class="ans">It is expected that you take all your math/stat courses at Hunter. The Department’s
                    rotation schedule
                    tells you how often courses are given so that you can plan
                    ahead. If it becomes necessary for you take a course elsewhere, the course must have the same
                    prerequisites as the
                    Hunter course. You need a permit approved
                    by a departmental advisor. Permits for CUNY campuses are now handled online in CUNY's e-permit
                    system.</p>
            </div>
            <div class="faq-item">
                <p class="question bold">I have been exempt from the math proficiency test. Why am I having difficulty
                    registering for
                    MATH
                    101 (or MATH 104,
                    MATH 125, STAT 113)? <i class="fas fa-angle-down"></i></p>
                <p class="ans">
                    Students “exempt from the math proficiency test” are exempt only from those parts of the test
                    required for admission to
                    Hunter. These students may register for MATH 100, MATH 102, or STAT 110 provided that one of these
                    is the mathematics
                    course they need.
                    The Department of Mathematics and Statistics uses other parts of the Math Test to place students in
                    its other
                    entry-level math/stat courses,
                    such as MATH 101, MATH 104, MATH 125, MATH 150, STAT 113, STAT 212, and STAT 213. If you wish to
                    register for one of
                    these courses, you must have the
                    prerequisite for the course – the prerequisite is either a preceding course or placement by the
                    Math Test.
                </p>
            </div>
            <div class="faq-item">
                <p class="question bold">I have the required COMPASS Math Test scores for entry into MATH 101. Should I
                    register for MATH
                    101? <i class="fas fa-angle-down"></i></p>
                <p class="ans">
                    Even though you are eligible to take MATH 101, you should not register for MATH 101 unless you need
                    the course for your
                    program of study. Is MATH 101 a prerequisite for a course that you need to take? Note that MATH 101
                    does not count
                    toward the General Education Requirement (GER).
                </p>
            </div>
            <div class="faq-item">
                <p class="question bold">How can I try to get into a closed section of a math/stat course? <i class="fas fa-angle-down"></i></p>
                <p class="ans">
                    You can keep trying to register in case spaces become available. There are no waiting lists to
                    register for closed
                    sections. If a section allows overtallies, you must
                    wait until the first meeting of the class. On the first day of classes, pick up an overtally form
                    near 919East, go to
                    the class, and see if the instructor is willing to sign it.
                    If you obtain a signature, return the form to 919East. Overtallying is left to the discretion of
                    the instructor.
                    Departmental advisors are not allowed to overtally.
                </p>
            </div>
            <div class="faq-item">
                <p class="question bold">Are there math/stat courses that do not allow overtallies? <i class="fas fa-angle-down "></i></p>
                <p class="ans">
                    Yes. Overtallies are not allowed in STAT 113 and STAT 214 because these courses require computer
                    terminals.
                </p>
