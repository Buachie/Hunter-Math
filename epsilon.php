                <h3>Recent Activities</h3>
                <p>
                    Each semester, PME sponsors a Math Jeopardy competition at Hunter College. Jeopardy is a TV game
                    show
                    where
                    the
                    problems are given in the form of an answer and the answer is given in the form of a question.
                    There
                    are 5
                    categories
                    and 5 problems for each category. Each problem is actually an answer to a question. The contestant
                    must
                    guess what the
                    corresponding question is for the given answer. For example:

                    <ul>
                        <li>Question: What is Pi?</li>
                        <li>Answer: It is the ratio of the circumference of a circle to its diameter.</li>
                    </ul>
                </p>
                <h3>Some History</h3>
                <p>
                    Pi Mu Epsilon was founded on May 25, 1914. It was the outgrowth of a mathematics club at Syracuse
                    University. Professor
                    Edward Drake Roe, Jr. had seen the benefit of intercollegiate cocurricular activities in Europe and
                    suggested that idea
                    to his club at Syracuse. He became the first Pi Mu Epsilon President, serving 1914-1929. The
                    history of
                    Pi
                    Mu Epsilon
                    was published in early volumes of the <a href="http://www.pme-math.org/organization/history.html">Pi
                        Mu
                        Epsilon
                        Journal.</a> Here is an anecdote from Pi Mu Epsilon's
                    history:
                </p>
                <p>
                    The tenth Chapter of Pi Mu Epsilon was established at Hunter College in New York City on Saturday,
                    May
                    30,
                    1925. The
                    installing officer of the Chapter was Pi Mu Epsilon's first President, E. D. Roe. In my files I
                    recently
                    found
                    Professor Roe's June 4, 1925 letter to the Secretary-Treasurer reporting on the installation. I
                    would
                    like
                    to share
                    with you a few sentences from that letter:
                </p>
                <p class="italic">
                    "It was pretty strenuous work for me. I had to get up at 4:30 a.m. and arrived home the next
