            <h2>Mathematics Major Curriculum</h2>
            <p>To enter the standard Mathematics Major, the student should have completed one year of calculus (MATH
                150
                and 155,
                or the equivalent). The major consists of at least 33 credits of coursework: 24 credits of core
                curriculum
                courses
                (taken by all majors except those enrolled in the concentration Preparation for Childhood Education:
                Grades
                1-6)
                and at least 9 credits of advanced elective courses, chosen by students according to their career
                plans.
                Proficiency in symbolic computation is also required.</p>

