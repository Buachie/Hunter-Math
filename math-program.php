            <h3>Elective Courses</h3>
            <p>
                The student must complete three 3-credit approved elective courses. The elective courses must be chosen
                from the 300-or
                400-level courses in mathematics or statistics listed by the department. Factors affecting the choice
                of courses will
                include the student’s career goals (preparation for graduate study; education; careers in business,
                government service
                or industry), the student’s other major, where applicable, and the availability of courses due to the
                course rotation
                schedule. A 300-level course may be replaced by a graduate level course approved by a departmental
                advisor.
            </p>

            <h3>Symbolic Computation Proficiency Requirement </h3>
            <p>
                As a requirement for graduation with a BA in Mathematics, students must demonstrate entry level
                proficiency in
                symbolic computation. The requirement can be met in any of the following ways:
                a) passing any of MATH 126, MATH 151, MATH 154, MATH 385;
                b) passing a departmental exam in a computer algebra system (currently we use MATHEMATICA or the
                equivalent).
            </p>

